// Logical AND operator (&&)
let x = 5;
let y = 10;
if (x > 0 && y > 0) {
    console.log("Both x and y are greater than 0");
} else {
    console.log("At least one of x and y is not greater than 0");
}

// Logical OR operator (||)
let a = 15;
let b = 20;
if (a > 10 || b > 10) {
    console.log("At least one of a and b is greater than 10");
} else {
    console.log("Neither a nor b is greater than 10");
}

// Logical NOT operator (!)
let flag = true;
if (!flag) {
    console.log("Flag is false");
} else {
    console.log("Flag is true");
}